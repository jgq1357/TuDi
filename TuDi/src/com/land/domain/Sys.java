package com.land.domain;

public class Sys {
	private String rowsOfLog;
	private String rowsOfSearch;
	private String logLevel;
	private String logPath;
	public String getLogLevel() {
		return logLevel;
	}
	public void setLogLevel(String logLevel) {
		this.logLevel = logLevel;
	}
	public String getLogPath() {
		return logPath;
	}
	public void setLogPath(String logPath) {
		this.logPath = logPath;
	}
	public String getRowsOfLog() {
		return rowsOfLog;
	}
	public void setRowsOfLog(String rowsOfLog) {
		this.rowsOfLog = rowsOfLog;
	}
	public String getRowsOfSearch() {
		return rowsOfSearch;
	}
	public void setRowsOfSearch(String rowsOfSearch) {
		this.rowsOfSearch = rowsOfSearch;
	}
	
	
}
